#!/bin/bash
DIR="blog/media/videos/tmp"
mkdir -p "$DIR"

texts=(
"交易盈利的唯一途徑是低買高賣。但價格為什麼會移動？波動是市場「意見不合」的體現。當買方的貪婪壓倒了賣方的恐懼，價格向上位移；反之亦然。記住，風險是利潤的入場券。沒有百分之百的勝率，只有盈虧比的博弈。"
"「繁榮」往往是欺騙性的信號。當價格加速衝刺、成交量暴增，大眾情緒被點燃時，往往是大資金撤退製造的「虛假繁榮」。同樣，極度的恐慌拋售也可能預示著底部的到來。真正的機會，往往誕生於繁榮枯竭後的反轉。"
"新手看指標，老手看狀態。激揚態：高點抬升，低點上移，展現出強大的韌性。萎靡態：重心下移，反彈無力，任何「便宜」都是誘餌。能量消散：當K線實體收縮，說明推動力已耗盡，是從看多轉為觀望的關鍵時刻。"
"支撐與阻力不是死板的線，而是千萬交易者的心理防線。歷史極值和成本牆構成了大眾共識。當曾經的阻力被強力突破，它會轉化為未來的支撐。這就是市場最迷人的角色轉換，也是「好地段」的自我修復。"
"再好的獵人，在不熟悉的森林裡也會迷路。你必須了解所選品種的「性格」，並將大周期的戰略與小周期的戰術相結合。如果你在小周期迷失方向，請回到大周期看清趨勢。"
"複利的前提是「活著」。倉位不取決於你想賺多少，而取決於你能賠多少。遵循「2%準則」，將每筆交易的風險嚴格鎖定。倉位控制不是為了限制盈利，而是為了在市場扇你耳光時，你只是臉紅一下，而不是直接暈倒。"
"一個完整的交易閉環是：看勢、選位、防騙、控倉。相信你的眼睛，克制你的慾望，在概率的遊戲中穩步前行。"
)

for i in "${!texts[@]}"; do
    num=$((i+1))
    echo "Generating audio $num..."
    say -v "TingTing" -o "$DIR/${num}_audio.aiff" "${texts[$i]}"
    ffmpeg -y -i "$DIR/${num}_audio.aiff" "$DIR/${num}_audio.mp3"
done
